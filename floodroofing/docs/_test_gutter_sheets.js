// Verify the per-gutter "Sheet: X.XXm" labels render after
// calibration on a plain outline (no manual sections). The user's
// flow: draw outline → auto-gen hip/valley → calibrate. We mimic
// that by seeding an outline, four gutter lines, a ridge, and a
// scaleMetresPerPx.

const { chromium } = require('playwright');
const path = require('path');

(async () => {
  const browser = await chromium.launch({ executablePath: '/opt/pw-browsers/chromium-1194/chrome-linux/chrome' });
  const ctx = await browser.newContext({ deviceScaleFactor: 2, viewport: { width: 1600, height: 1100 } });
  const page = await ctx.newPage();
  page.on('pageerror', e => console.error('PAGE_ERROR:', e.message));
  page.on('console', m => { if (m.type() === 'error') console.error('console:', m.text()); });
  await page.goto('file://' + path.resolve('floodroofing/frontend/index.html'), { waitUntil: 'domcontentloaded' });
  await page.evaluate(() => {
    document.querySelector('.app').style.display = '';
    document.getElementById('login-screen').style.display = 'none';
    // Simple rectangular outline (image coords).
    const A=[200,150], B=[620,150], C=[620,460], D=[200,460];
    window.DRAW.outline = [A,B,C,D];
    window.DRAW.outlineDone = true;
    window.DRAW.scaleMetresPerPx = 0.015;
    window.DRAW.calPitch = 18;
    // Four gutters around the outline.
    window.DRAW.lines = [
      { type:'gutter', pts:[A.slice(), B.slice()], label:'', measM:null, lengthM:'', sheetLengthM:null, autoGenerated:true },
      { type:'gutter', pts:[B.slice(), C.slice()], label:'', measM:null, lengthM:'', sheetLengthM:null, autoGenerated:true },
      { type:'gutter', pts:[C.slice(), D.slice()], label:'', measM:null, lengthM:'', sheetLengthM:null, autoGenerated:true },
      { type:'gutter', pts:[D.slice(), A.slice()], label:'', measM:null, lengthM:'', sheetLengthM:null, autoGenerated:true },
      // Central ridge (acts like an auto-generated ridge between two hip endpoints)
      { type:'ridge',  pts:[[300, 305], [520, 305]], label:'', measM:null, lengthM:'', sheetLengthM:null, autoGenerated:true },
      // Hip lines from the four corners to the ridge endpoints
      { type:'hip',    pts:[A.slice(), [300, 305]], label:'', measM:null, lengthM:'', sheetLengthM:null, autoGenerated:true },
      { type:'hip',    pts:[B.slice(), [520, 305]], label:'', measM:null, lengthM:'', sheetLengthM:null, autoGenerated:true },
      { type:'hip',    pts:[C.slice(), [520, 305]], label:'', measM:null, lengthM:'', sheetLengthM:null, autoGenerated:true },
      { type:'hip',    pts:[D.slice(), [300, 305]], label:'', measM:null, lengthM:'', sheetLengthM:null, autoGenerated:true }
    ];
    window.setTool && window.setTool('select');
    window.redrawAll && window.redrawAll();
    document.getElementById('canvasWrap').scrollIntoView({block:'start'});
  });
  await page.waitForTimeout(250);
  // After scroll, redraw so labels position correctly relative to viewport
  await page.evaluate(() => window.redrawAll && window.redrawAll());
  await page.waitForTimeout(120);
  await page.screenshot({ path: 'floodroofing/docs/redesign_v15_sheetlen.png', fullPage: false });
  console.log('wrote redesign_v15_sheetlen.png');
  await browser.close();
})().catch(e => { console.error(e); process.exit(1); });
