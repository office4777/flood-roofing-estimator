// Simulate the user flow: draw an outline, switch to Move, then
// programmatically dispatch a mousedown/mousemove/mouseup sequence on
// a corner to verify corner-drag still works.

const { chromium } = require('playwright');
const path = require('path');

(async () => {
  const browser = await chromium.launch({ executablePath: '/opt/pw-browsers/chromium-1194/chrome-linux/chrome' });
  const ctx = await browser.newContext({ deviceScaleFactor: 2, viewport: { width: 1600, height: 1100 } });
  const page = await ctx.newPage();
  const errors = [];
  page.on('pageerror', e => { errors.push('PAGE_ERROR: ' + e.message); });
  page.on('console', m => { if (m.type() === 'error') errors.push('console: ' + m.text()); });

  await page.goto('file://' + path.resolve('floodroofing/frontend/index.html'), { waitUntil: 'domcontentloaded' });

  // Load a fake bg image before evaluating so DRAW.bgImg has a real
  // Image with natural dimensions, otherwise drawImage throws.
  await page.evaluate(async () => {
    document.querySelector('.app').style.display = '';
    document.getElementById('login-screen').style.display = 'none';
    const tc = document.createElement('canvas');
    tc.width = 900; tc.height = 620;
    const tctx = tc.getContext('2d');
    tctx.fillStyle = '#eef'; tctx.fillRect(0, 0, 900, 620);
    const dataUrl = tc.toDataURL();
    await new Promise(res => {
      const img = new Image();
      img.onload = () => { window.DRAW.bgImg = img; res(); };
      img.src = dataUrl;
    });
  });

  const result = await page.evaluate(() => {
    // Outline + tool + calibrate so corner-drag should be live.
    window.DRAW.outline = [[200,150],[620,150],[620,460],[200,460]];
    window.DRAW.outlineDone = true;
    window.DRAW.scaleMetresPerPx = 0.015;
    window.DRAW.calPitch = 18;
    window.DRAW.lines = [
      { type:'gutter', pts:[[200,150],[620,150]], label:'', measM:null, lengthM:'', sheetLengthM:null, autoGenerated:true },
      { type:'gutter', pts:[[620,150],[620,460]], label:'', measM:null, lengthM:'', sheetLengthM:null, autoGenerated:true },
      { type:'gutter', pts:[[620,460],[200,460]], label:'', measM:null, lengthM:'', sheetLengthM:null, autoGenerated:true },
      { type:'gutter', pts:[[200,460],[200,150]], label:'', measM:null, lengthM:'', sheetLengthM:null, autoGenerated:true },
      { type:'ridge',  pts:[[300, 305], [520, 305]], label:'', measM:null, lengthM:'', sheetLengthM:null, autoGenerated:true },
      { type:'hip',    pts:[[200,150], [300, 305]], label:'', measM:null, lengthM:'', sheetLengthM:null, autoGenerated:true },
      { type:'hip',    pts:[[620,150], [520, 305]], label:'', measM:null, lengthM:'', sheetLengthM:null, autoGenerated:true },
      { type:'hip',    pts:[[620,460], [520, 305]], label:'', measM:null, lengthM:'', sheetLengthM:null, autoGenerated:true },
      { type:'hip',    pts:[[200,460], [300, 305]], label:'', measM:null, lengthM:'', sheetLengthM:null, autoGenerated:true }
    ];
    window.setTool('select');
    window.redrawAll();

    // Find the canvas + transform so we know where corner [200,150] is in client coords.
    const cv = document.getElementById('roofCanvas');
    const rect = cv.getBoundingClientRect();
    const t = window.getImgTransform();
    if (!t) return { ok:false, error:'no transform — bgImg missing?' };
    // Canvas coord of corner (200,150) — image pt × scale + offset, then divide by (canvasWidth / rect.width)
    const dpr = window.devicePixelRatio || 1;
    const cornerCanvas = [200 * t.s + t.ix, 150 * t.s + t.iy];
    // Convert canvas px to client px (rect.width / cv.width × dpr)
    const clientX = rect.left + cornerCanvas[0] * (rect.width / cv.width) * dpr;
    const clientY = rect.top  + cornerCanvas[1] * (rect.height / cv.height) * dpr;

    // Dispatch mousedown on that point — should set DRAG_OUTLINE_VERT
    cv.dispatchEvent(new MouseEvent('mousedown', { bubbles:true, clientX, clientY, button:0 }));
    const dragStarted = !!window.DRAG_OUTLINE_VERT;

    // Drag 30px right
    cv.dispatchEvent(new MouseEvent('mousemove', { bubbles:true, clientX: clientX + 30, clientY, button:0 }));
    const movedCornerX = window.DRAW.outline[0][0];

    // Release
    cv.dispatchEvent(new MouseEvent('mouseup', { bubbles:true, clientX: clientX + 30, clientY, button:0 }));
    const dragCleared = !window.DRAG_OUTLINE_VERT;

    return {
      ok: true,
      dragStarted, dragCleared,
      movedCornerX,
      tool: window.DRAW.tool,
      outlineDone: window.DRAW.outlineDone,
      outlineLen: window.DRAW.outline.length
    };
  });

  console.log('result:', JSON.stringify(result, null, 2));
  if (errors.length) console.log('errors:', errors);
  await browser.close();
})();
